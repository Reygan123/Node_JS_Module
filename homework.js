exports.luasPersegi = (sisi1, sisi2) => {
  return sisi1 * sisi2;
};

exports.kelilingPersegi = (sisi) => {
  return 4 * sisi;
};

exports.luasPersegiPanjang = (panjang, lebar) => {
  return panjang * lebar;
};

exports.kelilingPersegiPanjang = (panjang, lebar) => {
  return 2 * panjang + 2 * lebar;
};
